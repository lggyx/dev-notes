// string
let myName: string = 2015; // 数字不是字符串
let sayHi: string = [2016]; // 数组不是字符串

// number
let binary: number = '0b1010'; // 字符串不是数字
let octal: number = '0o744'; // 字符串不是数字
let decimal: number = '10'; // 字符串不是数字
let hex: number = '0xf00d'; // 字符串不是数字
let infinity: number = `Infinity`; // 模板字符串不是数字

// boolean
let done: boolean = 0; // 0、1不是布尔值

// undefined
let undefinedName: string = undefined; // undefined不是字符串

// null
let nullNumber: number = null; // null不是数字
